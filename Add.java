public class Add {
    public static void main(String[] args) {
        // Print a message
        System.out.println("Hello, World!");

        // Declare two numbers
        int a = 10;
        int b = 20;

        // Add the numbers
        int sum = a + b;

        // Print the result
        System.out.println("The sum of " + a + " and " + b + " is: " + sum);
    }
}
